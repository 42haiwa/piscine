#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b);

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	ba;
	int	bb;

	ba = *a;
	bb = *b;
	*a /= bb;
	*b = ba % bb;
}
